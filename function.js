var num1 = 13;
var num2 = 2;



function addTwoNumbers (num1, num2){
    var result = num1 + num2;
    return result;
}
var result = addTwoNumbers(num1, num2);
console.log("The addition is:", result);

function subtractTwoNumbers(num1, num2){
    var result = num1 - num2;
    return result;
}
var result = subtractTwoNumbers(num1, num2);
console.log("The subtraction is:", result);

function multiplyTwoNumbers(num1, num2){
    var result = num1 * num2;
    return result;
}
var result = multiplyTwoNumbers(num1, num2);
console.log("The multiplication is:", result);

function divideTwoNumbers(num1, num2){
    var result = num1 / num2;
    return result;
}
var result = divideTwoNumbers(num1, num2);
